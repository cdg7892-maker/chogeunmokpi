function reviewSet(title: string, cases: string[]): { title: string; patient: string }[] {
  const patients = ["김**", "박**", "이**"];
  return cases.map((caseTitle, index) => ({
    title: `[${title}] ${caseTitle}`,
    patient: patients[index] ?? "정**",
  }));
}

